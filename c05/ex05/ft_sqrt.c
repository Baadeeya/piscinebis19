int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	n;

	if (nb < 1)
		return (0);
	i = 1;
	n = nb;
	while (i * i < n)
		i++;
	if (i * i == n)
		return (i);
	else
		return (0);
}
